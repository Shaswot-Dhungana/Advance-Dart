
// Main Function

void main()
{

    int count = 1;

// While Loop

    while (count <= 10) {

        print("Current Count Number is  $count");

        count++;



        if (count == 8) {
print("Current Count Number is $count and Now Loop is Breaked.");

            break;

        }

    }

    print("Now you are out of while loop");
}
